# ─────────────────────────────────────────────
# 1. 補助方案主表
# ─────────────────────────────────────────────
cursor.execute('''
CREATE TABLE subsidy_list (
    id          INTEGER PRIMARY KEY AUTOINCREMENT,
    title       TEXT NOT NULL,   -- 補助名稱
    category    TEXT,            -- 分類：藝文 / 運動 / 旅遊 / 居住 / 就業 / 生活（用於興趣推薦）
    use_type    TEXT,            -- 四大分類：點數 / 津貼 / 場館 / 節慶（用於左側動線拆分）
    icon        TEXT,            -- 顯示用 emoji
    min_age     INTEGER,         -- 最小年齡
    max_age     INTEGER,         -- 最大年齡（999 = 不限）
    target_city TEXT,            -- 限定縣市（全台 = 通用）
    amount      TEXT,            -- 補助額度（文字描述）
    start_date  TEXT,            -- 開始/開放日期
    deadline    TEXT,            -- 截止日期
    status      TEXT,            -- 狀態：開放領取 / 登記抽籤 / 即將開放 / 已結束
    description TEXT,            -- 白話懶人包（一句話講重點）
    how_to      TEXT,            -- 怎麼領（步驟）
    notes       TEXT,            -- 加碼優惠 / 注意事項
    url         TEXT,            -- 官方連結
    value_min   INTEGER DEFAULT 0,   -- 補助價值下限（NT$，給省錢試算，保守估計）
    value_max   INTEGER DEFAULT 0,   -- 補助價值上限（NT$）
    age_flex    INTEGER DEFAULT 0    -- 1=有身分別例外（不完全依年齡），資格顯示為「需確認」
)
''')

# ─────────────────────────────────────────────
# 2. 使用者表（Google 登入後同步）
# ─────────────────────────────────────────────
cursor.execute('''
CREATE TABLE users (
    google_id TEXT PRIMARY KEY,
    email     TEXT,
    name      TEXT,
    picture   TEXT,
    age       INTEGER,   -- 問卷後更新
    city      TEXT,      -- 問卷後更新
    interest  TEXT       -- 興趣（藝文/運動/旅遊/居住/就業），問卷後更新
)
''')

# ─────────────────────────────────────────────
# 3. 消費店家表（給「消費地圖」用）
# ─────────────────────────────────────────────
cursor.execute('''
CREATE TABLE stores (
    id        INTEGER PRIMARY KEY AUTOINCREMENT,
    name      TEXT,      -- 店家/場館名稱
    coin_type TEXT,      -- 可用的券別：藝文 / 運動 / 旅遊
    city      TEXT,      -- 縣市
    district  TEXT,      -- 行政區
    address   TEXT,      -- 地址
    lat       REAL,      -- 緯度
    lng       REAL,      -- 經度
    note      TEXT       -- 小提示
)
''')

# ─────────────────────────────────────────────
# 4. 領取紀錄表（給「行程規劃」用，記錄使用者領了哪些券）
# ─────────────────────────────────────────────
cursor.execute('''
CREATE TABLE user_claims (
    id         INTEGER PRIMARY KEY AUTOINCREMENT,
    google_id  TEXT,
    subsidy_id INTEGER,
    claimed_at TEXT DEFAULT (datetime('now','localtime')),
    UNIQUE(google_id, subsidy_id)
)
''')

